var bg_org;   
$(function(){	
	$('.btnk').css('background-color','#c0c0c0');	
    $('.btnk').hover(
        function(){  
            bg_org = $(this).css('background-color');  
            $(this).css('background-color','#ffe4b5'); },  
        function(){ 
            $(this).css('background-color',bg_org);			
			}			
    );
	
	
});
